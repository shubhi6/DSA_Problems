#include<iostream>
using namespace std;
     // bitwise AND operator
     
     
int main()
{
	int x=3; int y=6;
	cout<<(x&y);
	return 0;
}
    // bitwise OR operator
    
    
int main()
{
	int x=3; int y=6;
	cout<<(x|y);
	return 0;
}
     // bitwise XOR operator
     
     
int main()
{
	int x=3; int y=6;
	cout<<(x^y);
	return 0;
}
